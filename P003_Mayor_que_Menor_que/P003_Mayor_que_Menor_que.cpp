#include <iostream>
#include <math.h>


using namespace std;

int main()
{
    
    float num1 = 0;
    float num2 = 0;
    cout << " Hola usuario, dame un numero\n";
    cin >> num1;
    cout << "Dame otro numero\n";
    cin >> num2;
   
    if (num1 > num2)
    {
        std::cout << num1 << " es mayor a " << num2 << std::endl;
    }
    if (num1 < num2)
    {
        std::cout << num1 << " es menor a " << num2 << std::endl;
    }
    if (num1 == num2)
    {
        std::cout << num1 << " es igual a " << num2 << std::endl;
    }
}
		
		
	
	
	
	
	
	

	



	














